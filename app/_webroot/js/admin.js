function toTop(url, id)
{
  var btn = $('#btn' + id);
  btn.button('loading')

  new $.ajax({
    url: url,
    type: "GET",
    dataType: "text",
    success: success(btn, id),
    error: error(btn),
    complete: complete(btn, id)
  });
}

function success(btn, id)
{
  setTimeout(function () {
    btn.replaceWith('<span class="label label-success" id="label' + id + '">完了</span>');
  }, 1000)
}
function error(btn)
{
  setTimeout(function () {
    btn.button('error')
  }, 1000)
}
function complete(btn, id)
{
	setTimeout(function () {
		var cnt = $.ajax({
			url: "/shop/get_to_top_count/" + id + "/",
			async: false
		}).responseText;
		if (parseInt(cnt) == 0) {
			// alert('上限に達しました');
			var limitHTML = '<div style="display:inline-block" data-toggle="tt" data-placement="top" title="上限に達しました。">';
			    limitHTML += '<button id="btn' + id + '" class="btn btn-default btn-sm" disabled="disabled">上位表示</button>';
			    limitHTML += '</div>';
			$('#label' + id).replaceWith(limitHTML);
			$('#compare-count' + id).text('残' + cnt + '回');
		} else {
			$('#label' + id).replaceWith('<a href="javascript:void(0);" id="btn' + id + '" class="btn btn-sm btn-warning" data-loading-text="更新中..." onclick="toTop(\'/shop/to_top/' + id + '/\', ' + id + '); return false;" autocomplete="off">上位表示</a>');
			$('#compare-count' + id).text(cnt);
		}
	}, 1000)
}

